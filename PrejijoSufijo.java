public class PrejijoSufijo {
	public static void main(String args[]) {
		//incremento con sufijo.
		int x = 34;
		int y = x++;

		System.out.println("ela valor de y es "+y);
		System.out.println("el valor de x es "+x);
	
    //incremento con prefijo.

	int z = 34;
	int a = ++z;
	System.out.println("el valor de a es "+a);
    System.out.println("el valor de la z es "+z);
	//decremento con sufijo.

	int b = 32;
	int c =b--;

	System.out.println("el valor de la c es "+c);
    System.out.println("el valor de b es "+b);

	//decremento con prefijo.

	int d = 32;
	int e = --d;

	System.out.println("el valor de e es "+e);
	System.out.println("el valor de la d es "+d);



     }
}